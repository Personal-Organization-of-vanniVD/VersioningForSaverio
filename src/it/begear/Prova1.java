package it.begear;

public class Prova1 {

	private int x;
	private int y;
	private String s;
	private boolean tof;
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public String getS() {
		return s;
	}
	
	public void setS(String s) {
		this.s = s;
	}
	
	public boolean isTof() {
		return tof;
	}
	
	public void setTof(boolean tof) {
		this.tof = tof;
	}

	
	
}